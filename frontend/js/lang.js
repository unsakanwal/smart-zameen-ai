// ─── THE ONE FUNCTION THAT TRANSLATES EVERYTHING ───
function setLang(lang, btn) {
  const t = LANG[lang] || LANG['en'];

  // Set text content by element ID (skips silently if element not on this page)
  const set = (id, val) => {
    const el = document.getElementById(id);
    if (el && val !== undefined) el.textContent = val;
  };

  // Set placeholder attribute
  const setPH = (id, val) => {
    const el = document.getElementById(id);
    if (el && val !== undefined) el.placeholder = val;
  };

  // RTL / LTR direction
  document.documentElement.dir = ['ur','pa','sd','ps'].includes(lang) ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;

  // ── Navbar (all pages) ──
  set('app-name', t.app);
  set('t-home',   t.home);
  set('t-pred',   t.pred);
  set('t-dash',   t.dash);
  // Update the Languages button text without touching the dropdown arrow CSS trick
  const langToggleEl = document.getElementById('langToggle');
  if (langToggleEl) langToggleEl.textContent = t.lang_btn;

  // ── Home page ──
  const heroTitleEl = document.getElementById('hero-title');
  if (heroTitleEl && t.hero_title) heroTitleEl.innerHTML = t.hero_title;
  set('hero-desc',    t.hero_desc);
  set('btn-predict',  t.btn_predict);
  set('btn-dashboard',t.btn_dashboard);
  set('stat-crops',   t.stat_crops);
  set('stat-accuracy',t.stat_accuracy);
  set('stat-languages',t.stat_languages);
  set('feat-tag',     t.feat_tag);
  set('feat-title',   t.feat_title);
  set('feat-desc',    t.feat_desc);
  set('f1-title', t.f1_title); set('f1-desc', t.f1_desc);
  set('f2-title', t.f2_title); set('f2-desc', t.f2_desc);
  set('f3-title', t.f3_title); set('f3-desc', t.f3_desc);
  set('f4-title', t.f4_title); set('f4-desc', t.f4_desc);
  set('f5-title', t.f5_title); set('f5-desc', t.f5_desc);
  set('f6-title', t.f6_title); set('f6-desc', t.f6_desc);
  set('footer-copy',  t.footer_copy);
  set('footer-info',  t.footer_info);

  // ── Predict page ──
  set('pred-title',       t.pred_title);
  set('pred-desc',        t.pred_desc);
  set('form-title',       t.form_title);
  set('label-nitrogen',   t.label_nitrogen);
  set('label-phosphorus', t.label_phosphorus);
  set('label-potassium',  t.label_potassium);
  set('label-ph',         t.label_ph);
  set('label-temp',       t.label_temp);
  set('label-rainfall',   t.label_rainfall);
  set('label-region',     t.label_region);
  set('label-season',     t.label_season);
  set('btn-predict-ai',   t.btn_predict_ai);
  set('loading-text',     t.loading_text);
  set('crop-yield',       t.crop_yield);
  set('crop-best-time',   t.crop_best_time);
  set('crop-water',       t.crop_water);
  set('confidence',       t.confidence);
  
  // Farmer friendly elements
  set('farmer-title', t.farmer_title);
  set('farmer-desc', t.farmer_desc);
  set('q-nitrogen', t.q_nitrogen);
  set('q-ph', t.q_ph);
  set('q-temp', t.q_temp);
  set('q-rainfall', t.q_rainfall);
  set('q-potassium', t.q_potassium);
  set('btn-predict-farmer', t.btn_predict_farmer);

  // Dropdown options with data-key
  document.querySelectorAll('option[data-key]').forEach(opt => {
    const val = t[opt.getAttribute('data-key')];
    if (val) opt.textContent = val;
  });

  // ── Dashboard page ──
  set('dash-title',        t.dash_title);
  set('user-name',         t.user_name);
  set('alert-msg',         t.alert_msg);
  set('current-crop',      t.current_crop);
  set('crop-wheat',        t.crop_wheat);
  set('crop-area',         t.crop_area);
  set('crop-status',       t.crop_status);
  set('predicted-yield',   t.predicted_yield);
  set('yield-value',       t.yield_value);
  set('yield-per',         t.yield_per);
  set('today-weather',     t.today_weather);
  set('weather-humidity',  t.weather_humidity);
  set('weather-rainfall',  t.weather_rainfall);
  set('weather-wind',      t.weather_wind);
  set('weather-temp',      t.weather_temp);
  set('soil-condition',    t.soil_condition);
  set('soil-nitrogen',     t.soil_nitrogen);
  set('soil-phosphorus',   t.soil_phosphorus);
  set('soil-potassium',    t.soil_potassium);
  set('ai-recommendation', t.ai_recommendation);
  set('accuracy-label',    t.accuracy_label);

  // ── Login page ──
  set('login-title',       t.login_title);
  set('login-desc',        t.login_desc);
  set('forgot-password',   t.forgot_password);
  set('signin-btn',        t.signin_btn);
  set('welcome-title',     t.welcome_title);
  set('welcome-desc',      t.welcome_desc);
  setPH('email',           t.email_placeholder);
  setPH('password',        t.password_placeholder);

  // ── Signup page ──
  set('signup-title',      t.signup_title);
  set('signup-desc',       t.signup_desc);
  set('signup-btn',        t.signup_btn);
  set('welcome-back',      t.welcome_back);
  set('welcome-back-desc', t.welcome_back_desc);
  set('signin-link',       t.signin_link);
  setPH('name',            t.name_placeholder);

  // ── Mark active option in dropdown ──
  document.querySelectorAll('.lang-option').forEach(b => b.classList.remove('on'));
  if (btn) {
    btn.classList.add('on');
  } else {
    // highlight the right option without a button reference
    document.querySelectorAll('.lang-option').forEach(b => {
      if (b.getAttribute('onclick') && b.getAttribute('onclick').includes(`'${lang}'`)) {
        b.classList.add('on');
      }
    });
  }

  // ── Persist the choice so every page loads in the same language ──
  localStorage.setItem('sz_lang', lang);

  // If there is an active soil analysis displayed, re-render it in the new language
  if (window.lastSoilData && typeof showSoilResult === 'function') {
    showSoilResult(window.lastSoilData);
  }
}

// ─── AUTO-APPLY SAVED LANGUAGE ON EVERY PAGE LOAD ───
window.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('sz_lang') || 'en';
  setLang(saved, null);
});